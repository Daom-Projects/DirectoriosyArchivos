    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        nombreDir = jTextField1.getText();
        File laRuta = new File(carpeta.getAbsolutePath()+"/"+nombreDir);
        
        if (!laRuta.exists()) {
            System.out.println("Creando El Directorio: " + nombreDir);
            boolean result = false;
            try{
                laRuta.mkdirs();
                result = true;
            }catch(SecurityException se){
                JOptionPane.showMessageDialog(rootPane, "Error al crear la carpeta", "Error", JOptionPane.ERROR_MESSAGE);
            }        
            if(result) {
                JOptionPane.showMessageDialog(rootPane, "Directorio Creado", "Operacion Completada", JOptionPane.INFORMATION_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(rootPane, "El Directorio ya existe", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        
        jFileChooser2.setFileFilter(new FileNameExtensionFilter("Archivos TIF", "tif", "jpg"));
        
        int returnVal = jFileChooser1.showOpenDialog(this);
        if (returnVal == jFileChooser1.APPROVE_OPTION) {
            carpeta = jFileChooser1.getSelectedFile();
            try {
                // What to do with the file, e.g. display it in a TextArea
                jLabel3.setText(carpeta.getAbsolutePath());
            } catch (Exception ex) {
                System.out.println("Problemas al seleccionar la carpeta"+carpeta.getAbsolutePath());
            }
        } else {
            System.out.println("Accion cancelada por el usuario");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

