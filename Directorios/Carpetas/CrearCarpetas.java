    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        File Destino = new File(carpeta+"\\"+nombreDir+"\\"+archivo.getName());
        try {
            Files.copy(archivo.toPath(), Destino.toPath());
            JOptionPane.showMessageDialog(rootPane, "Archivo "+archivo.getName()+" Copiado correctamente", "Operacion Copiar Completada", JOptionPane.INFORMATION_MESSAGE);
        } catch (IOException ex) {
            Logger.getLogger(CrearCarpetas.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(rootPane,"Problemas al copiar el archivo: "+ex.getMessage(), "Error al Copiar", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

